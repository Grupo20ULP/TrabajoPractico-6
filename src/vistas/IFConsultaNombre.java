    private void llenarTabla() {
        prueba.setRowCount(0);
        String textoBusqueda = txtBusquedaNombre.getText().toLowerCase();

        for (Producto productoLlenar : VistaPrincipal.listaProductos) {
            if (textoBusqueda.isEmpty() || productoLlenar.getDescripcion().toLowerCase().contains(textoBusqueda)) {
                prueba.addRow(new Object[]{
                    productoLlenar.getCodigo(),
                    productoLlenar.getDescripcion(),
                    productoLlenar.getPrecio(),
                    productoLlenar.getRubro(),
                    productoLlenar.getStock()
                });
            }
        }
    }
